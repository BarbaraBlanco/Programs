#include <stdio.h>

int main(){
    // Declaração de um vetor de inteiros com tamamho 5
    int vetor[5] = {1, 2, 3, 4, 5};

    // Acesso e impressão do terceiro elemento (índice 2) do vetor
    printf("O terceiro elemento do vetor e: %d\n", vetor[2]);


    // Declaração de uma matriz de inteiros 2x3
    int matriz[2][3] = {{1, 2, 3}, {4, 5, 6}};

    // Acesso e impressão do elemento na segunda linha e terceira coluna (índices 1 e 2)
    printf("O elemento na segunda linha e terceira coluna e: %d\n", matriz[1][2]);


return 0; }
